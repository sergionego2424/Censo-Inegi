    private void btnBucarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBucarActionPerformed
          HabitanteDecorator habitante = new HabitanteDecorator().getHabitante(txtCURP.getText());
          
          
          txtNombre.setText(habitante.nombre);
          txtPaterno.setText(habitante.paterno);
          txtMaterno.setText(habitante.materno);
          txtSexo.setText(habitante.sexo);
          
          cmbDia.setSelectedItem(habitante.fecha_nacimiento.split("/")[0]);
          cmbMes.setSelectedItem(habitante.fecha_nacimiento.split("/")[1]);
          cmbYear.setSelectedItem(habitante.fecha_nacimiento.split("/")[2]);
          
          cmbCasado.setSelectedItem(habitante.casado);
          
          cmbVivienda.setSelectedIndex(habitante.vivienda.id_vivienda);
          
          txtCalle.setText(habitante.vivienda.calle);
          txtNumero.setText(habitante.vivienda.numero);
          txtColonia.setText(habitante.vivienda.colonia);
          txtCodigoPostal.setText(habitante.vivienda.codigo_postal);
          
          cmbTipoVivienda.setSelectedIndex(habitante.vivienda.id_tipo_vivienda );
          cmbLocalidad.setSelectedIndex(habitante.vivienda.id_AGEEML );
          
    }//GEN-LAST:event_btnBucarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        HabitanteDecorator modelo_habitante = new HabitanteDecorator();
        modelo_habitante.habitante = new HabitanteDecorator() ;
        modelo_habitante.habitante.CURP = txtCURP.getText();
        modelo_habitante.habitante.nombre = txtNombre.getText();
        modelo_habitante.habitante.paterno = txtPaterno.getText();
        modelo_habitante.habitante.materno = txtMaterno.getText();
        
        
        
        
        modelo_habitante.habitante.fecha_nacimiento = cmbDia.getSelectedItem().toString() +  "/" + 
                                     cmbMes.getSelectedItem().toString() + "/" +
                                    cmbYear.getSelectedItem().toString();
       
        modelo_habitante.habitante.sexo = txtSexo.getText();
        modelo_habitante.habitante.casado = cmbCasado.getSelectedItem().toString();
        //int id_vivienda, String calle, String numero, String colonia,String codigo_postal,int tipo_vivienda,int id_AGEEML
        modelo_habitante.vivienda = new Vivienda(
                    cmbVivienda.getSelectedIndex(),
                    txtCalle.getText(),
                    txtNumero.getText(),
                    txtColonia.getText(),
                    txtCodigoPostal.getText(),
                    cmbTipoVivienda.getSelectedIndex(),
                    cmbLocalidad.getSelectedIndex()
            );
        
        
      
        
        
        modelo_habitante.insertHabitante();
        
    }//GEN-LAST:event_btnGuardarActionPerformed

