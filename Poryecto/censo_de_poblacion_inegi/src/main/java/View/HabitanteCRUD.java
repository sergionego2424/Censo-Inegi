    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            Vivienda[] viviendas = Vivienda.getViviendas();
            
            System.out.println(viviendas[0].id_vivienda);
            for(int i = 0 ; i < viviendas.length ; i++){
                cmbVivienda.addItem(viviendas[i].colonia + " " +  viviendas[i].calle + " " + viviendas[i].numero);
            }
            
            LinkedList<String> tipo_viviendas = Vivienda.getTipoVivienda();
            for(int i = 0; i < tipo_viviendas.size(); i ++){
                  cmbTipoVivienda.addItem(tipo_viviendas.get(i));
            }
            
            
            LinkedList<String> localidades = Vivienda.getLocalidad();
            for(int i = 0; i < localidades.size(); i ++){
                  cmbLocalidad.addItem(localidades.get(i));
            }
        } catch (SQLException ex) {
            Logger.getLogger(HabitanteCRUD.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

