    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        HabitanteActicidadEconomicaDecorator habitante_modelo = new HabitanteActicidadEconomicaDecorator();
        habitante_modelo.habitante = new HabitanteActicidadEconomicaDecorator();
        habitante_modelo.habitante.CURP = cmbCURP.getSelectedItem().toString();
        habitante_modelo.descripcion_actividad = txtDescripcion.getText();
        habitante_modelo.remuneracion = Float.parseFloat(txtRemuneracion.getText());
        
        try {
            habitante_modelo.agregarActividadEconomica();
        } catch (SQLException ex) {
            Logger.getLogger(ActividadEconomica.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        HabitanteActicidadEconomicaDecorator habitante = new HabitanteActicidadEconomicaDecorator();
        
        try {
            LinkedList<String> CURP = habitante.getCURP();
            for(int i = 0 ; i < CURP.size() ; i ++){
                cmbCURP.addItem(CURP.get(i));
            }
        } catch (SQLException ex) {
            Logger.getLogger(ActividadEconomica.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

